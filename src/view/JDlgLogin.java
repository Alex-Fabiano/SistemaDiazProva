    private void JBtnOk1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnOk1ActionPerformed
        // TODO add your handling code here:

        String usuario = JTxtUser.getText();
        String senha = jPwfSenha_afl.getText();

        JFrmPrincipal principal = new JFrmPrincipal();

        if (usuario.equals("aluno") && senha.equals("123")) {
            JOptionPane.showMessageDialog(null, "Login bem-sucedido!");
            principal.setVisible(true);
            this.dispose();
        } else {

            JOptionPane.showMessageDialog(null, "Usuário ou senha inválidos.");
            principal.setVisible(false);
        }


    }//GEN-LAST:event_JBtnOk1ActionPerformed

